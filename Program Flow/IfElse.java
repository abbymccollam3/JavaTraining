public class IfElse {
    public static void main(String[] args) {

        int monthNum = 4;

        if (monthNum >= 1 && monthNum <= 3) {
                System.out.println("You're in Q1");

        }   else if (monthNum >= 4 && monthNum <= 6) {
                System.out.println("You're in Q2");
        
        }   else {
                System.out.println("Unknown");

        }
        
    }
}
